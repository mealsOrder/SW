/**
 * Object / 객체
 */
// key:value pair
let yuJin = {
    name: '안유진',
    group: '아이브',
    dance: function(){
        return `${this.name}이 춤을 춥니다.`;
    }
};
console.log(yuJin);
console.log(yuJin.name);    // 같은결과
console.log(yuJin['name']); // 같은 결과

const key = 'name';
console.log(yuJin.name);
console.log(yuJin[key]);
console.log(yuJin.dance());

const nameKey = 'name';
const nameValue = '안유진';

