/**
 * Variable 선언하기
 * 
 * 1) var - 더이상 쓰지 않는다.
 * 2) let
 * 3) const
 */

var name = '코드팩토리';
console.log(name);

var age = 29;
console.log(age);

let ive = 'ghdl';
console.log(ive);

/**
 * let과 var로 선언하면
 * 값을 추후 변경할 수 있다.
 */

ive = '안유진';
console.log(ive);

const newJeans = '뉴진스';
console.log(newJeans);

//newJeans = '나';
/**
 * const 키워드로 변수를 선언하면
 * 변수를 추후에 변경 할 수 없다.
 */

/**
 * 선언과 할당
 * 
 * 1) 변수를 선언하는 것.
 * 2) 할당
 * 
 */

var name = '아저시';
console.log(name);

let my;
console.log(my); // undefined

